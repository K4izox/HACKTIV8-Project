            const retBtn = document.createElement('button'); retBtn.className = 'meta-btn'; retBtn.innerHTML = '<i class="fa-solid fa-rotate-right"></i>';
            retBtn.title = 'Retry';
            retBtn.addEventListener('click', () => retryMsg(wrap));
